package main

import "fmt"

func main() {
	a := 20
	b := 10
	result := a + b
	fmt.Println(result)
	result1 := a - b
	fmt.Println(result1)
	result2 := a * b
	fmt.Println(result2)
	result3 := a / b
	fmt.Println(result3)
	var result4 float64 = 3.0 / 2.0
	fmt.Println(result4)
	result5 := a % b
	fmt.Println(result5)
}
